module instr_mem (
	input [31:0] addr,
	output [31:0] instr
);

logic [31:0] mem [0:255];

assign instr = mem[addr[9:2]];

initial begin

    // ==========================================
    // R-Type + Setup
    // ==========================================

    mem[0]  = 32'h01400093; // addi x1,x0,20
    mem[1]  = 32'h00500113; // addi x2,x0,5

    mem[2]  = 32'h002081B3; // add x3,x1,x2
    mem[3]  = 32'h40208233; // sub x4,x1,x2
    mem[4]  = 32'h0020C2B3; // xor x5,x1,x2
    mem[5]  = 32'h0020E333; // or x6,x1,x2
    mem[6]  = 32'h0020F3B3; // and x7,x1,x2
    mem[7]  = 32'h00211433; // sll x8,x2,x2
    mem[8]  = 32'h0020D4B3; // srl x9,x1,x2
    mem[9]  = 32'h00112533; // slt x10,x2,x1
    mem[10] = 32'h001135B3; // sltu x11,x2,x1

    // ==========================================
    // I-Type
    // ==========================================

    mem[11] = 32'h0050C613; // xori x12,x1,5
    mem[12] = 32'h0020E693; // ori x13,x1,2
    mem[13] = 32'h0070F713; // andi x14,x1,7

    mem[14] = 32'h00109793; // slli x15,x1,1
    mem[15] = 32'h0010D813; // srli x16,x1,1

    mem[16] = 32'hFF800893; // addi x17,x0,-8
    mem[17] = 32'h4018D913; // srai x18,x17,1

    mem[18] = 32'h0190A993; // slti x19,x1,25
    mem[19] = 32'h0190BA13; // sltiu x20,x1,25

    // ==========================================
    // Memory
    // ==========================================

    mem[20] = 32'h00102023; // sw x1,0(x0)
    mem[21] = 32'h00002A83; // lw x21,0(x0)

    // ==========================================
    // BEQ
    // ==========================================

    mem[22] = 32'h00108463; // beq x1,x1,+8
    mem[23] = 32'h06300B13; // addi x22,x0,99
    mem[24] = 32'h00100B13; // addi x22,x0,1

    // ==========================================
    // BNE
    // ==========================================

    mem[25] = 32'h00209463; // bne x1,x2,+8
    mem[26] = 32'h06300B93; // addi x23,x0,99
    mem[27] = 32'h00200B93; // addi x23,x0,2

    // ==========================================
    // BLT
    // ==========================================

    mem[28] = 32'h00114463; // blt x2,x1,+8
    mem[29] = 32'h06300C13; // addi x24,x0,99
    mem[30] = 32'h00300C13; // addi x24,x0,3

    // ==========================================
    // BGE
    // ==========================================

    mem[31] = 32'h0020D463; // bge x1,x2,+8
    mem[32] = 32'h06300C93; // addi x25,x0,99
    mem[33] = 32'h00400C93; // addi x25,x0,4

    // ==========================================
    // JAL
    // ==========================================

    mem[34] = 32'h00800D6F; // jal x26,+8
    mem[35] = 32'h06300D93; // addi x27,x0,99
    mem[36] = 32'h00700D93; // addi x27,x0,7

    // ==========================================
    // JALR setup
    // Jump target = address 160
    // 160 / 4 = mem[40]
    // ==========================================

    mem[37] = 32'h0A000E13; // addi x28,x0,160
    mem[38] = 32'h000E0EE7; // jalr x29,x28,0
    mem[39] = 32'h06300F13; // addi x30,x0,99

    // ==========================================
    // Fill NOPs until mem[40]
    // ==========================================

    mem[40] = 32'h00800F13; // addi x30,x0,8

end
endmodule
